#include <iostream>
using namespace std;

int main() {
    //print numbers from 1 to 10
    cout << "For Loop ";
    for (int i = 1; i < 11; i++) {
        cout << i << " ";
    }
    cout << endl;

    cout << "While Loop ";
    int i = 1;
    while (i < 11) {
        cout << i << " ";
        i++;
    }
    cout << endl;
    
}